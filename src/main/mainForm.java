    void loadData(){
        try {
            String sql = "select id,nom,prenom,date_of_birth,phone_number,cin,date_embauche,status,base_salary from employee_list";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while (table.getRowCount() > 0) {                
                ((DefaultTableModel)table.getModel()).removeRow(0);
            }
            int column = rs.getMetaData().getColumnCount();
            while (rs.next()) {                
                Object[] row = new Object[column];
                for (int i = 1; i <= column; i++) {
                    row[i-1] = rs.getObject(i);
                }
                ((DefaultTableModel)table.getModel()).insertRow(rs.getRow() -1, row);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    void printCount(){
        try {
            String sql = "select count(id) from employee_list";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                lblTotal.setText(rs.getString("count(id)")+".00");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            String sql = "SELECT count(status) FROM `employee_list` WHERE `status` = \"Actif\"";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                lblActive.setText(rs.getString("count(status)")+".00");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            String sql = "SELECT count(status) FROM `employee_list` WHERE `status` = \"Inactif\"";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                lblInactive.setText(rs.getString("count(status)")+".00");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
        
