    private void txtSearch2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearch2KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                String sql = "SELECT * FROM `employee_list` WHERE `id` = ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, txtSearch2.getText());
                rs = ps.executeQuery();
                if (rs.next()) {
                    txtFname2.setText(rs.getString(3));
                    txtLname2.setText(rs.getString(2));
                    txtDepartment2.setText(rs.getString(9));
                    txtDesignation2.setText(rs.getString(10));
                    txtNumber2.setText(rs.getString(5));
                    if (rs.getString(12).equals("Actif")) {
                        r_Active2.setSelected(true);
                        r_InActive2.setSelected(false);
                    } else if (rs.getString(12).equals("Inactif")){
                        r_InActive2.setSelected(true);
                        r_Active2.setSelected(false);
                    }
                    txtAddress2.setText(rs.getString(6));
                    txtSalary2.setText(rs.getString(13));
                } else {
                    JOptionPane.showMessageDialog(null, "Employee Id not found in database");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_txtSearch2KeyPressed

