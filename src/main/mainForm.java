    private void rechercherMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rechercherMousePressed
        // TODO add your handling code here:
        try{

            String sql ="select * from employee_list where id=? ";

            ps=conn.prepareStatement(sql);
            int id = Integer.parseInt(search_emp.getText());
            ps.setInt(1, id);
            ps.setString(1,search_emp.getText());
            rs=ps.executeQuery();
            
            if (rs.next()==false){
                JOptionPane.showMessageDialog(this, "Pas de resultat");
                search_id.setText("");
                search_lname.setText("");
                search_fname.setText("");
                search_cin.setText("");
                search_dob.setText("");
                search_doj.setText("");
                search_phone.setText("");
                search_address.setText("");
                search_sexe.setText("");
                search_status.setText("");
                search_dep.setText("");
                search_fonction.setText("");
                search_salaire.setText("");
                search_id.requestFocus();
            } else {
                search_id.setText(rs.getString("id"));
                search_lname.setText(rs.getString("nom"));
                search_fname.setText(rs.getString("prenom"));
                search_cin.setText(rs.getString("cin"));
                search_dob.setText(rs.getString("date_of_birth"));
                search_doj.setText(rs.getString("date_embauche"));
                search_phone.setText(rs.getString("phone_number"));
                search_address.setText(rs.getString("address"));
                search_sexe.setText(rs.getString("sexe"));
                search_status.setText(rs.getString("status"));
                search_dep.setText(rs.getString("departement"));
                search_fonction.setText(rs.getString("job_title"));
                search_salaire.setText(rs.getString("base_salary"));
                
                byte[] img = rs.getBytes("image");
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH));
                lbl_img.setIcon(imageIcon);
            }

            

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Missing Data");
        }
        finally {

            try{

                rs.close();
                ps.close();

            }
            catch(Exception e){

            }
        }
    }//GEN-LAST:event_rechercherMousePressed

